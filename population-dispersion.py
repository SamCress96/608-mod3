import statistics
#Calculate the popopulation variance
statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])
print('population variance is:', statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))

#Calculate the population standard deviation 
statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])
print('population standard deviation is:', statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))